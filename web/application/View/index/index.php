<h1>Welcome SDS v1.0.0 Admin Portal</h1>
<h3>SDS Admin User Functions</h3>
<p>SDS v1.0.0.0 is an SMS dating service is a cloud hosted SMS dating solution that allows the user to do the following: </p>
<ul>
    <li><strong>Subscribe or Register </strong> - the service allows subscribers to register via SMS. </li>
    <li><strong>Manage User Profile</strong> – uses can pause and resume service via SMS.</li>
    <li><strong>Opt Out of Service</strong> – users can opt out of the service via SMS</li>
    <li><strong>Request for help</strong> – users can request for help via SMS.</li>
    <li><strong>Receive Date Matching</strong> – the solution does an intelligent match and share at most one date match per day.</li>
    <li><strong>Receive Dating Tips</strong> – in case a user does not have a matching date in the system, the solution will send out a dating tip at most one per day.</li>
    <li><strong>Receive Info Messages</strong> – users whose profile is not fully registered will receive an information SMS at most one per day requesting them to update their profile.</li>
</ul>

<h3>SDS Admin User Functions</h3>
<p>The SDS Admin Portal v.1.0.0 allows the admin use to carry out the following functionalities:</p>
<ul>
    <li><strong>Manage Customers</strong> - an admin user can query and view customer's profile, update their profile (including pausing and resuming service). The user can also view all system interactions related to a customer e.g. all messages received, all messages sent out, etc.</li>
    <li><strong>Manage Dating Tips</strong> - an admin use can add, edit, delete, query dating tips. The admin user can also put a dating tip offline or online.</li>
    <li><strong>Management of System Task</strong> - an admin user can view system tasks (background tasks e.g. matchin task run in the system) and their execution status. An admin user can also submit a matching task.</li>
</ul>

